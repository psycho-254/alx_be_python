# Area of a rectangle

# define two vaiables to represent the dimensions of a rectangle
# lenght to represent the length of the rectangle
# width to represent the width of the rectangle

length = 10
width = 5
# Calculate the area of the rectangle using the formula: Area = Length * Width
area = length * width
# Print the calculated area in a human-readable format
print(f"The area of the rectangle is: {area}")