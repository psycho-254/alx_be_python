# User Input Age Calculator

# prompt the user for their current age
current_age = int(input("How old are you? "))

# calculate the user's age in the year 2050

difference = 2050 - 2023  # Calculate the difference in years from 2023 to 2050

age = current_age + difference # Add the difference to the current age

print(f"In 2050, you will be {age} years old") #print output in human readable format